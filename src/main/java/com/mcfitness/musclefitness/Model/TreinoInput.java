package com.mcfitness.musclefitness.Model;

public class TreinoInput {

        private Integer id;
        private String nome;
        private Integer exercicio;
        private Integer aluno;
        private String repeticoes;
        private String velocidade;
        private String descanso;
        private Integer musculoAlvo;
        private Integer diaSemana;
        private String series;
        private Integer objetivo;
        public Integer getId() {
                return id;
        }
        public void setId(Integer id) {
                this.id = id;
        }
        public String getNome() {
                return nome;
        }
        public void setNome(String nome) {
                this.nome = nome;
        }
        public Integer getExercicio() {
                return exercicio;
        }
        public void setExercicio(Integer exercicio) {
                this.exercicio = exercicio;
        }
        public Integer getAluno() {
                return aluno;
        }
        public void setAluno(Integer aluno) {
                this.aluno = aluno;
        }
        public String getRepeticoes() {
                return repeticoes;
        }
        public void setRepeticoes(String repeticoes) {
                this.repeticoes = repeticoes;
        }
        public String getVelocidade() {
                return velocidade;
        }
        public void setVelocidade(String velocidade) {
                this.velocidade = velocidade;
        }
        public String getDescanso() {
                return descanso;
        }
        public void setDescanso(String descanso) {
                this.descanso = descanso;
        }
        public Integer getMusculoAlvo() {
                return musculoAlvo;
        }
        public void setMusculoAlvo(Integer musculoAlvo) {
                this.musculoAlvo = musculoAlvo;
        }
        public Integer getDiaSemana() {
                return diaSemana;
        }
        public void setDiaSemana(Integer diaSemana) {
                this.diaSemana = diaSemana;
        }
        public String getSeries() {
                return series;
        }
        public void setSeries(String series) {
                this.series = series;
        }
        public Integer getObjetivo() {
                return objetivo;
        }
        public void setObjetivo(Integer objetivo) {
                this.objetivo = objetivo;
        }
        
        
}